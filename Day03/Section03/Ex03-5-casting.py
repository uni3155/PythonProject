'''
    Casting
        변수에 유형을 지정하려는 경우 캐스팅으로 가능.
'''

# 정수형
x = int(1)
print(x)
y = int(2.8)
print(y)
z = int("3")
print(z)
print(type(z))
print(x+z)

# 실수형
x = float(1)
print(x)
x = float("3")
print(x)

# 문자형
x = str(1)
y = str(2)
print(x)
print(x+y)

# 문자를 아스키코드 값으로 변환
a = ord('A')
print(a)
# 아스키코드 값을 문자로 변환
b = chr(65)
print(b)


